# Operadores aritméticos
# Soma
soma = 9 + 4
print(soma)
# Subtração
Subtracao = 6 - 3
print(Subtracao)
# Multiplicação
multiplicacao = 2 * 7
print(multiplicacao)
# Divisão
Divisao = 5/3
print(Divisao)
# Divisão inteira
Divisao_inteiro = 10 // 5
print(Divisao_inteiro)
# Exponenciação
exponenciacao = 9 ** 3
print (exponenciacao)
# Módulo / Resto da Divisão
modulo = 8 % 1
print(modulo)